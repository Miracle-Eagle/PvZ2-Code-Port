namespace Global::Values
{
/// <summary> Determines the Property which belongs to a Entity. </summary>

enum class EntityKey
{
/// Property refers to Entity's Hitpoints.
Hitpoints,

/// Property refers to the DPS (Damage per Second) a Zombie does while Eating.
EatDPS,

/// Property refers to Entity's Speed.
Speed,

/// Property refers to the Wave Point Cost of a Zombie.
WavePointCost,

/// Property refers to the Mastery Boost Chance of a Plant.
MasteryBoostChance,

/// Property refers to the Mastery Resistance Piercing of a Plant.
MasteryResistancePiercing
};

}
