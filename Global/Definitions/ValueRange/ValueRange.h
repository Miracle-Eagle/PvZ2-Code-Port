namespace Global::Definitions
{
/// <summary> Stores two Floating Values which Represent the Minimum and the Maximum Index of a Range. </summary>

class ValueRange
{

public:

/** <summary> Gets or Sets a Minimum Value for a Range. </summary>
<returns> The Minimum Range Value. </returns> */

float Min;

/** <summary> Gets or Sets a Maximum Value for a Range. </summary>
<returns> The Maximum Range Value. </returns> */

float Max;

/// <summary> Creates a new Representation of the ValueRange Class. </summary>

ValueRange();
};

}